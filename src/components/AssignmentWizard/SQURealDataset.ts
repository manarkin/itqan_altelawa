// Custom CSV Parser to dynamically create SQU candidate students matching SQU timetable
export function parseSQUCSV(csvText: string): any[] {
  const result: any[] = [];
  const lines = csvText.trim().split('\n');

  // Skip the first row (header)
  for (let i = 1; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line) continue;

    // Custom CSV parser that cleanly respects quotes
    const fields: string[] = [];
    let insideQuotes = false;
    let currentField = '';

    for (let charIdx = 0; charIdx < line.length; charIdx++) {
      const char = line[charIdx];
      if (char === '"') {
        insideQuotes = !insideQuotes;
      } else if (char === ',' && !insideQuotes) {
        fields.push(currentField);
        currentField = '';
      } else {
        currentField += char;
      }
    }
    fields.push(currentField);

    if (fields.length < 9) continue;

    const id = fields[0]?.trim();
    const cohort = fields[1]?.trim();
    const college = fields[2]?.trim();
    const levelRaw = fields[3]?.trim();

    const timings: Record<string, string> = {};

    const parseDaySlots = (day: string, text: string) => {
      if (!text || text.includes('لا يناسبني')) return;
      const hours = text.toLowerCase();
      // Look for occurrences of hours and map to official 8 timeslot keys
      if (hours.includes('8:00') || hours.includes('٨:٠٠')) {
        timings[`${day}_8:00-9:15`] = 'selected';
      }
      if (hours.includes('10:00') || hours.includes('١٠:٠٠')) {
        timings[`${day}_10:00-11:15`] = 'selected';
      }
      if (hours.includes('12:00') || hours.includes('١٢:٠٠')) {
        timings[`${day}_12:00-1:15`] = 'selected';
      }
      if (hours.includes('2:00') || hours.includes('٢:٠٠')) {
        timings[`${day}_2:15-3:30`] = 'selected';
      }
      if (hours.includes('4:00') || hours.includes('٤:٠٠') || hours.includes('مساءً')) {
        timings[`${day}_4:15-5:30`] = 'selected';
      }
    };

    parseDaySlots('Sunday', fields[4]);
    parseDaySlots('Monday', fields[5]);
    parseDaySlots('Tuesday', fields[6]);
    parseDaySlots('Wednesday', fields[7]);
    parseDaySlots('Thursday', fields[8]);

    // Omani First, Father and Surnames for realistic rendering matching Sultan Qaboos context
    const OMANI_FIRST_NAMES = [
      'فاطمة', 'عائشة', 'ريم', 'منى', 'كوثر', 'أروى', 'غالية', 'عهود', 'بلقيس', 
      'ابتسام', 'هناء', 'مريم', 'أسماء', 'خلود', 'أميرة', 'بثينة', 'روان', 'شمسة', 
      'تسنيم', 'مزون', 'إيمان', 'وفاء', 'منال', 'رحمة', 'شيماء', 'هدى', 'حليمة'
    ];
    const OMANI_FATHER_NAMES = ['خلفان', 'سليمان', 'ناصر', 'سعيد', 'محمد', 'أحمد', 'جمعة', 'راشد', 'حارب', 'حمد', 'سالم'];
    const OMANI_FAMILY_NAMES = [
      'الشرجية', 'الرحبية', 'الجابرية', 'الهنائية', 'البوسعيدية', 'الحارثية', 
      'الغيلانية', 'المعمرية', 'العبرية', 'الشكيلية', 'الدرعية', 'السنيدية', 
      'السيابية', 'الشنفرية', 'الريامية', 'اليحمدية', 'الخروصية', 'الكندية'
    ];

    const fName = OMANI_FIRST_NAMES[i % OMANI_FIRST_NAMES.length];
    const fatherName = OMANI_FATHER_NAMES[(i + 3) % OMANI_FATHER_NAMES.length];
    const lastName = OMANI_FAMILY_NAMES[(i + 7) % OMANI_FAMILY_NAMES.length];

    // Determine level
    let parsedLevel = 'مبتدئة';
    if (levelRaw.includes('متقدمة') || levelRaw.includes('حفظ') || levelRaw.includes('مجازة')) {
      parsedLevel = 'متقدمة';
    } else if (levelRaw.includes('تمهيدية') || levelRaw.includes('تلاوة')) {
      parsedLevel = 'تمهيدية';
    } else if (levelRaw.includes('مبتدئة') || levelRaw.includes('مخارج')) {
      parsedLevel = 'مبتدئة';
    } else {
      parsedLevel = 'مبتدئة'; // fallback for 'لم يسبق لي الالتحاق' or other
    }

    // undergrad vs postgraduate/employee
    const isPostgrad = cohort.includes('الخامسة') || cohort.includes('السادسة') || cohort.includes('السابعة') || cohort.includes('غير ذلك');
    const studentType = isPostgrad ? 'postgrad' : 'undergrad';

    result.push({
      firstName: fName,
      fatherName: fatherName,
      grandfatherName: 'سعيد',
      lastName: lastName,
      name: `${fName} ${lastName}`,
      role: 'STUDENT',
      phone: `+968 9660 ${Math.floor(1000 + Math.random() * 9000)}`,
      email: `s${id}@student.squ.edu.om`,
      studentId: id,
      level: parsedLevel,
      college: college,
      cohort: cohort,
      approved: true,
      isNew: levelRaw.includes('لم يسبق'),
      studentType: studentType,
      preferredFormat: studentType === 'postgrad' ? 'online' : 'in-person',
      enrollmentDetails: {
        timings: timings,
        preferredSessionFormat: studentType === 'postgrad' ? 'online' : 'in-person'
      }
    });
  }

  return result;
}
